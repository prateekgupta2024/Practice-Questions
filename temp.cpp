class Solution {
  public:
    int getMinDiff(int arr[], int n, int k) {
        // code here
        int i,a[n];
        for(i=0;i<n;i++)
        {
            if(arr[i]-k>=0)
            a[i]=arr[i]-k;
            else
            a[i]=arr[i]+k;
        }
        sort(a,a+n);
        return a[n-1]-a[0];
    }
};

